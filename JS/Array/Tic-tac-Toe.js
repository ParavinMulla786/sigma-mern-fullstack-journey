//Tic-Tac-Toe game using Array
// X      Null    O
// Null   X       Null
// O      Null    X


// Using null values
let game = [
    ["X", null, "O"],
    [null, "X", null],
    ["O", null, "X"]
];

console.log(game);


// Using empty spaces instead of null
let game1 = [
    ["X", " ", "O"],
    [" ", "X", " "],
    ["O", " ", "X"]
];

console.log(game1);
